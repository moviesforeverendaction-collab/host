def _write_nginx(config_name: str, content: str):
    avail  = os.path.join(NGINX_AVAIL_DIR, config_name)
    linked = os.path.join(NGINX_SITES_DIR, config_name)
    with open(avail, "w") as f:
        f.write(content)
    if not os.path.islink(linked):
        os.symlink(avail, linked)


def _remove_nginx(config_name: str):
    for path in [os.path.join(NGINX_SITES_DIR, config_name),
                 os.path.join(NGINX_AVAIL_DIR, config_name)]:
        if os.path.islink(path) or os.path.exists(path):
            try: os.unlink(path)
            except: pass


def _reload_nginx():
    ok, out = _run("sudo /usr/sbin/nginx -t")
    if not ok:
        return False, f"Nginx config test failed:\n{out}"
    return _run("sudo /bin/systemctl reload nginx")

