def deploy_website(db, user_id: int, zip_path: str, project_name: str,
                   is_premium: bool, progress_cb=None) -> dict:
    limits    = get_website_limits(is_premium)
    site_name = _safe_name(project_name)

    base, suffix = site_name, 1
    while get_website(db, site_name):
        site_name = f"{base}-{suffix}"; suffix += 1

    subdomain = f"{site_name}.{BOT_DOMAIN}"
    site_dir  = os.path.join(SITES_BASE_DIR, site_name)

    try:
        _ensure_dirs()
        if progress_cb: progress_cb("📦 Extracting files...")

        with tempfile.TemporaryDirectory() as tmp:
            with zipfile.ZipFile(zip_path, "r") as z:
                total = sum(i.file_size for i in z.infolist())
                limit = limits["max_size_mb"] * 1024 * 1024
                if total > limit:
                    return {"success": False,
                            "message": f"❌ Zip too large: {total//(1024*1024)}MB (limit: {limits['max_size_mb']}MB)"}
                z.extractall(tmp)

            html_root = _find_html_root(tmp)
            if not html_root:
                return {"success": False, "message": "❌ No <code>index.html</code> found in zip."}

            if progress_cb: progress_cb("🚀 Copying files...")
            os.makedirs(site_dir, exist_ok=True)
            for item in os.listdir(html_root):
                src = os.path.join(html_root, item)
                dst = os.path.join(site_dir, item)
                (shutil.copytree if os.path.isdir(src) else shutil.copy2)(src, dst)

        _run(f"chmod -R 755 {site_dir}")
        _run(f"chown -R www-data:www-data {site_dir}")

        if progress_cb: progress_cb("⚙️ Configuring nginx + SSL...")

        _write_nginx(site_name, _nginx_subdomain_config(subdomain, site_dir))
        ok, out = _reload_nginx()
        if not ok:
            shutil.rmtree(site_dir, ignore_errors=True)
            _remove_nginx(site_name)
            return {"success": False, "message": f"❌ Nginx error:\n<code>{out}</code>"}

        if progress_cb: progress_cb("💾 Saving record...")
        create_website_record(db, user_id, site_name, subdomain, site_dir,
                              "Premium" if is_premium else "Free")

        return {
            "success":   True,
            "url":       f"https://{subdomain}",
            "subdomain": subdomain,
            "site_name": site_name,
            "ssl":       True,
            "message":   "✅ Website deployed!",
        }
    except Exception as e:
        shutil.rmtree(site_dir, ignore_errors=True)
        _remove_nginx(site_name)
        _reload_nginx()
        return {"success": False, "message": f"❌ Deployment failed: {e}"}

def update_website(db, site_name: str, zip_path: str,
                   is_premium: bool, progress_cb=None) -> dict:
    site = get_website(db, site_name)
    if not site:
        return {"success": False, "message": "❌ Website not found."}

    limits   = get_website_limits(is_premium)
    site_dir = site["site_dir"]

    try:
        with tempfile.TemporaryDirectory() as tmp:
            with zipfile.ZipFile(zip_path, "r") as z:
                total = sum(i.file_size for i in z.infolist())
                if total > limits["max_size_mb"] * 1024 * 1024:
                    return {"success": False,
                            "message": f"❌ Zip too large. Limit: {limits['max_size_mb']}MB"}
                z.extractall(tmp)

            html_root = _find_html_root(tmp)
            if not html_root:
                return {"success": False, "message": "❌ No index.html found."}

            if progress_cb: progress_cb("🔄 Updating files...")
            shutil.rmtree(site_dir, ignore_errors=True)
            os.makedirs(site_dir, exist_ok=True)
            for item in os.listdir(html_root):
                src = os.path.join(html_root, item)
                dst = os.path.join(site_dir, item)
                (shutil.copytree if os.path.isdir(src) else shutil.copy2)(src, dst)

        _run(f"chmod -R 755 {site_dir}")
        _run(f"chown -R www-data:www-data {site_dir}")
        update_website_record(db, site_name, {})
        return {"success": True, "message": "✅ Website updated!"}
    except Exception as e:
        return {"success": False, "message": f"❌ Update failed: {e}"}

def add_custom_domain(db, site_name: str, custom_domain: str, is_premium: bool) -> dict:
    if not is_premium:
        return {"success": False,
                "message": "❌ Custom domains are <b>Premium only</b>. Use /premium to upgrade."}

    site = get_website(db, site_name)
    if not site:
        return {"success": False, "message": "❌ Website not found."}

    custom_domain = custom_domain.lower().strip().rstrip(".")
    if not re.match(r"^([a-z0-9][a-z0-9\-]*\.)+[a-z]{2,}$", custom_domain):
        return {"success": False,
                "message": "❌ Invalid domain. Example: <code>mysite.com</code>"}

    existing = db.db["websites"].find_one({"custom_domain": custom_domain})
    if existing and existing["site_name"] != site_name:
        return {"success": False, "message": "❌ Domain already linked to another site."}

    site_dir    = site["site_dir"]
    config_name = f"{site_name}--custom"

    _write_nginx(config_name, _nginx_custom_domain_config(custom_domain, site_dir))
    ok, out = _reload_nginx()
    if not ok:
        _remove_nginx(config_name)
        return {"success": False, "message": f"❌ Nginx error:\n<code>{out}</code>"}

    ssl_ok, cert, key, ssl_msg = _issue_certbot_ssl(custom_domain, site_dir)
    if ssl_ok:
        _write_nginx(config_name, _nginx_custom_domain_config(
            custom_domain, site_dir, ssl=True, cert=cert, key=key))
        _reload_nginx()

    update_website_record(db, site_name, {
        "custom_domain": custom_domain,
        "custom_ssl":    ssl_ok,
    })

    return {
        "success":     True,
        "url":         f"{'https' if ssl_ok else 'http'}://{custom_domain}",
        "ssl":         ssl_ok,
        "ssl_message": ssl_msg,
        "message":     "✅ Custom domain linked!",
    }


def remove_custom_domain(db, site_name: str) -> dict:
    site = get_website(db, site_name)
    if not site:
        return {"success": False, "message": "❌ Website not found."}
    if not site.get("custom_domain"):
        return {"success": False, "message": "❌ No custom domain linked."}
    _remove_nginx(f"{site_name}--custom")
    _reload_nginx()
    update_website_record(db, site_name, {"custom_domain": None, "custom_ssl": False})
    return {"success": True, "message": "✅ Custom domain removed."}

def delete_website(db, site_name: str) -> dict:
    site = get_website(db, site_name)
    if not site:
        return {"success": False, "message": "❌ Website not found."}

    _remove_nginx(site_name)
    if site.get("custom_domain"):
        _remove_nginx(f"{site_name}--custom")
    _reload_nginx()

    site_dir = site.get("site_dir", "")
    if site_dir and os.path.exists(site_dir):
        shutil.rmtree(site_dir, ignore_errors=True)

    delete_website_record(db, site_name)
    return {"success": True, "message": "✅ Website deleted."}

def refresh_ssl(db, site_name: str) -> dict:
    site = get_website(db, site_name)
    if not site:
        return {"success": False, "message": "❌ Website not found."}

    results = []

    ok, out = _run("sudo /usr/bin/certbot renew --cert-name teamdev.sbs-0001 --force-renewal 2>&1", timeout=180)
    results.append(f"{'✅' if ok else '❌'} Wildcard SSL (*.{BOT_DOMAIN}): {'Renewed!' if ok else out[:120]}")
    if not ok:
        results.append("⚠️ Wildcard renewal requires DNS TXT record — run certbot manually.")

    if site.get("custom_domain"):
        ok2, out2 = _run(
            f"sudo /usr/bin/certbot renew --cert-name {site['custom_domain']} --force-renewal 2>&1",
            timeout=120
        )
        results.append(f"{'✅' if ok2 else '❌'} {site['custom_domain']}: {'Renewed!' if ok2 else out2[:120]}")

    _reload_nginx()
    return {"success": True, "message": "\n".join(results)}

def get_website_status(db, site_name: str) -> dict:
    site = get_website(db, site_name)
    if not site:
        return {}

    site_dir = site.get("site_dir", "")
    size_mb  = 0
    if os.path.exists(site_dir):
        total = sum(
            os.path.getsize(os.path.join(dp, f))
            for dp, _, files in os.walk(site_dir) for f in files
        )
        size_mb = round(total / (1024 * 1024), 2)

    subdomain  = site.get("subdomain", "")
    ok, _      = _run(f"grep -rl '{subdomain}' {NGINX_SITES_DIR} 2>/dev/null")

    return {
        "site_name":     site_name,
        "subdomain":     subdomain,
        "custom_domain": site.get("custom_domain"),
        "ssl_enabled":   True,
        "custom_ssl":    site.get("custom_ssl", False),
        "status":        site.get("status", "active"),
        "size_mb":       size_mb,
        "tier":          site.get("tier", "Free"),
        "created_at":    site.get("created_at"),
        "updated_at":    site.get("updated_at"),
        "nginx_active":  ok,
    }

def _issue_certbot_ssl(domain: str, site_dir: str):
    cmd = (
        f"sudo /usr/bin/certbot certonly --webroot -w {site_dir} "
        f"--non-interactive --agree-tos --email {CERTBOT_EMAIL} "
        f"-d {domain} 2>&1"
    )
    ok, out = _run(cmd, timeout=120)
    if ok:
        cert = f"/etc/letsencrypt/live/{domain}/fullchain.pem"
        key  = f"/etc/letsencrypt/live/{domain}/privkey.pem"
        return True, cert, key, "✅ SSL issued!"
    return False, "", "", f"SSL pending — DNS may not be propagated yet. ({out[:150]})"

def validate_domain(domain: str):
    ok, out = _run(f"dig +short A {domain} | head -1", timeout=10)
    if ok and out.strip():
        return True, f"DNS resolves to: {out.strip()}"
    return False, "No A record found. Point your domain's A record to this server's IP first."

def _find_html_root(directory: str):
    if os.path.exists(os.path.join(directory, "index.html")):
        return directory
    for folder in ["dist", "build", "public", "out", "www", "html", "static"]:
        path = os.path.join(directory, folder)
        if os.path.isdir(path) and os.path.exists(os.path.join(path, "index.html")):
            return path
    for root, _, files in os.walk(directory):
        if "index.html" in files:
            return root
    return None
